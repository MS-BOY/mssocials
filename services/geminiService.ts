export const suggestKeywords = async (text: string) => {
  const ai = getAI();
  const response = await ai.models.generateContent({
    model: 'gemini-3-flash-preview',
    contents: `Based on this text: "${text}", suggest 5 relevant hashtags or keywords. Return as a comma-separated list.`,
  });
  return response.text?.trim() || "";
};
